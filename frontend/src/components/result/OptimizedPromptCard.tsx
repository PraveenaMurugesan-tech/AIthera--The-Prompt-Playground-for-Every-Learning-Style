import React from 'react';
import { motion } from 'framer-motion';
import { Copy, RefreshCw, Share2, Download, CheckCircle2 } from 'lucide-react';

interface OptimizedPromptCardProps {
  prompt: string;
}

export const OptimizedPromptCard: React.FC<OptimizedPromptCardProps> = ({ prompt }) => {
  const [copied, setCopied] = React.useState(false);

  const handleCopy = () => {
    navigator.clipboard.writeText(prompt);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <motion.div 
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      className="bg-white dark:bg-slate-900 rounded-2xl shadow-lg border border-slate-200 dark:border-slate-800 p-6 md:p-8"
    >
      <div className="flex flex-col gap-6">
        <div>
          <h2 className="text-xl md:text-2xl font-bold text-slate-900 dark:text-white">
            Optimized Learning Prompt
          </h2>
          <p className="text-sm text-slate-500 dark:text-slate-400 mt-1">
            Generated by AIthera Council
          </p>
        </div>

        <div className="relative group">
          <div className="absolute -inset-0.5 bg-gradient-to-r from-blue-500 to-indigo-500 rounded-xl blur opacity-20 group-hover:opacity-40 transition duration-1000 group-hover:duration-200"></div>
          <div className="relative bg-slate-50 dark:bg-slate-950 rounded-xl p-6 border border-slate-200 dark:border-slate-800 min-h-[150px]">
            <p className="text-slate-800 dark:text-slate-200 whitespace-pre-wrap font-medium leading-relaxed">
              {prompt}
            </p>
          </div>
        </div>

        <div className="flex flex-wrap items-center gap-3 pt-2">
          <button
            onClick={handleCopy}
            className="flex items-center gap-2 px-4 py-2.5 bg-blue-600 hover:bg-blue-700 text-white rounded-lg font-medium transition-colors shadow-sm"
          >
            {copied ? <CheckCircle2 className="w-5 h-5" /> : <Copy className="w-5 h-5" />}
            {copied ? 'Copied!' : 'Copy Prompt'}
          </button>
          
          <button className="flex items-center gap-2 px-4 py-2.5 bg-white dark:bg-slate-800 hover:bg-slate-50 dark:hover:bg-slate-700 text-slate-700 dark:text-slate-200 border border-slate-200 dark:border-slate-700 rounded-lg font-medium transition-colors shadow-sm">
            <RefreshCw className="w-5 h-5" />
            Regenerate
          </button>
          
          <div className="flex-1"></div>

          <button className="p-2.5 text-slate-500 hover:text-slate-700 dark:text-slate-400 dark:hover:text-slate-200 bg-white dark:bg-slate-800 hover:bg-slate-50 dark:hover:bg-slate-700 border border-slate-200 dark:border-slate-700 rounded-lg transition-colors shadow-sm">
            <Share2 className="w-5 h-5" />
          </button>
          
          <button className="p-2.5 text-slate-500 hover:text-slate-700 dark:text-slate-400 dark:hover:text-slate-200 bg-white dark:bg-slate-800 hover:bg-slate-50 dark:hover:bg-slate-700 border border-slate-200 dark:border-slate-700 rounded-lg transition-colors shadow-sm">
            <Download className="w-5 h-5" />
          </button>
        </div>
      </div>
    </motion.div>
  );
};
